# 1. Преобразовать строку в нижний регистр для игнорирования регистра символов.
# 2. Удалить из строки все пробелы и неалфавитные символы (опционально, в зависимости от задачи).
# 3. Определить длину строки.
# 4. Создать цикл, который будет идти от начала строки до её середины.
# 5. В каждой итерации сравнивать i-й символ с n-i-1-м символом.
# 6. Если символы не равны, строка не является палиндромом. Вернуть False.
# 7. Если цикл завершился без обнаружения неравных символов, строка является палиндромом. Вернуть True.


def is_palindrome(s):
    s = s.lower()
    s = ''.join(filter(str.isalnum, s))
    n = len(s)
    for i in range(n // 2):
        if s[i] != s[n - i - 1]:
            return False
    return True


print(is_palindrome("A man, a plan, a canal, Panama"))  # Должно вернуть True
print(is_palindrome("Hello"))  # Должно вернуть False