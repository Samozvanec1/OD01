# создать переменную для хранения максимального числа в массиве
# перебираем массив через цикл
# Сравнение текущего элемента с максимальным значением в переменной
# если текущее значение больше максимального, то перезаписываем максимальное значение
# Возвращаем максимальное значение, после окончания цикла

def find_max(array):
    max = array[0]
    for i in range(0, len(array)):
        if array[i] > max:
            max = array[i]
    return max

# создать переменную для хранения минимального числа в массиве
# перебираем массив через цикл
# Сравнение текущего элемента с минимальным значением в переменной
# если текущее значение меньше минимального, то перезаписываем минимальное значение
# Возвращаем минимальное значение, после окончания цикла
def find_min(array):
    min = array[0]
    for i in range(0, len(array)):
        if array[i] < min:
            min = array[i]
    return min

