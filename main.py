# сравнение числа с нулём, если число равно нулю, то факториал нуля равен 1
# создание переменной для хранения итогового результата
# С помощью цикла for и range перебиаем список из чисел до нашего числа включительно, у которого мы ищем факториал
# результирующую переменную домножаем на текущее число в цикле
# возвращаем результирующую переменную


def find_factorial(number):
    if number == 0:
        return 1
    result = 1
    for i in range(1, number + 1):
        result *= i
    return result

# result = 1*1 =1
# result = 1*2 =2
